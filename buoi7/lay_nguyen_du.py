a = int(input("Nhập số nguyên dương a: "))
b = int(input("Nhập số nguyên dương b: "))
phan_nguyen = a // b
print("Phần nguyên của", a, "chia cho", b, "là:", phan_nguyen)
phan_du = a % b
print("Phần dư của", a, "chia cho", b, "là:", phan_du)
